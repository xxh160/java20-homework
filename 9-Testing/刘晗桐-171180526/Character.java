package Testing;

public class Character {
	public String name;
	public String gender;
	
	public void setName(String newName) {
		this.name = newName;
	}
	
	public void setGender(String newGender) {
		this.gender = newGender;
	}
	
	public String getName() {
		return this.name;
	}
	
	public String getGender() {
		return this.gender;
	}
}
